package legend.game.inventory.screens;

public enum TextColour {
  WHITE(0x7b, 0x7b, 0x7b),
  GREEN(0x0c, 0x3c, 0x00),
  LIME(0x2c, 0x5c, 0x00),
  CYAN(0x38, 0x7c, 0x7c),
  BROWN(0x2c, 0x20, 0x18),
  RED(0x6c, 0x20, 0x10),
  MIDDLE_BROWN(0x48, 0x34, 0x20),
  LIGHT_BROWN(0x50, 0x40, 0x28),
  YELLOW(0x7c, 0x7c, 0x00),
  BLACK(0x04, 0x04, 0x04),
  GREY(0x3c, 0x3c, 0x3c),
  PURPLE(0x30, 0x00, 0x7c),
  DARK_GREY(0x1b, 0x1b, 0x1b),

  FOOTER_BROWN(0x2c, 0x20, 0x18),
  FOOTER_WHITE(0x7b, 0x7b, 0x7b),
  FOOTER_GREY(0x5d, 0x5d, 0x5d),
  FOOTER_RED(0x6a, 0x3a, 0x3a),
  FOOTER_PINK(0x6a, 0x3a, 0x5c),
  FOOTER_PURPLE(0x56, 0x3a, 0x6a),
  FOOTER_BLUE(0x3a, 0x3d, 0x6a),
  FOOTER_AQUA(0x3a, 0x69, 0x6a),
  FOOTER_GREEN(0x3a, 0x6a, 0x44),
  FOOTER_LIME(0x53, 0x6a, 0x3a),
  FOOTER_YELLOW(0x6a, 0x6a, 0x3a),
  FOOTER_ORANGE(0x6a, 0x53, 0x3a),
  ;

  public final int r;
  public final int g;
  public final int b;
  public final int colour;

  TextColour(final int r, final int g, final int b) {
    this.r = r;
    this.g = g;
    this.b = b;
    this.colour = b << 16 | g << 8 | r;
  }
}
